	CREATE PROCEDURE INSERTAR_PROCURADOR
	@IDENTIFICACION_PROCURADOR BIGINT ,
	@NOMBRE_PROCURADOR VARCHAR (100),
	@TELEFONO_PROCURADOR BIGINT,
	@CORREO_PROCURADOR VARCHAR(100),
	@TIPO_PODER VARCHAR (100)
	AS
	INSERT INTO PROCURADOR
	VALUES (@IDENTIFICACION_PROCURADOR,@NOMBRE_PROCURADOR,@TELEFONO_PROCURADOR,@CORREO_PROCURADOR,@TIPO_PODER);

	EXEC INSERTAR_PROCURADOR 1,'LUIS',218432,'LUIS@GMAIL.COM','JURIDICO'
	EXEC INSERTAR_PROCURADOR 2,'MARIA',213243,'MARIA@HOTMAIL.COM', 'FEDERAL'
	
	CREATE PROCEDURE INSERTAR_CLIENTE
	@IDENTIFICACION_CLIENTE BIGINT,
	@NOMBRE_CLIENTE VARCHAR (100),
