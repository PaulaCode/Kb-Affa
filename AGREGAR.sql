	CREATE PROCEDURE INSERTAR_GABINETE
	@NOMBRE_CARSO VARCHAR(100),
	@DESCRIPCION_CASO VARCHAR(200),
	@FECHA_INICIO DATETIME,
	@FECHA_FIN DATETIME,
	@ESTADO_CASO INT,
